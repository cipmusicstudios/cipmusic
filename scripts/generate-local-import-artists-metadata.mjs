import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const SRC_DIR = path.join(__dirname, '../src');

// 1. We need the artist dictionary
const TARGET_FILE = path.join(SRC_DIR, 'local-import-artists-metadata.generated.ts');

async function importArtistDictionary() {
  const modPath = path.join(SRC_DIR, 'local-import-artist-normalization.ts');
  const code = fs.readFileSync(modPath, 'utf-8');
  // Hacky way to extract the dictionary without compiling typescript natively in this script
  const match = code.match(/export const ARTIST_DICTIONARY.*?=\s*(\{[\s\S]*?\});/);
  if (match && match[1]) {
    try {
      // Need to parse a generic Object literal loosely
      const fn = new Function('return ' + match[1]);
      return fn();
    } catch(e) {
      console.warn("Failed to parse dictionary directly, will rely on basic strings");
    }
  }
  return {};
}

async function fetchAppleMusicArtistImage(artistName) {
  try {
    const searchUrl = `https://itunes.apple.com/search?term=${encodeURIComponent(artistName)}&entity=musicArtist&limit=1`;
    const searchRes = await fetch(searchUrl);
    const searchData = await searchRes.json();

    if (!searchData.results || searchData.results.length === 0) {
      return null;
    }

    const artistLinkUrl = searchData.results[0].artistLinkUrl;
    if (!artistLinkUrl) return null;

    const pageRes = await fetch(artistLinkUrl);
    const html = await pageRes.text();

    const ogImageMatch = html.match(/<meta property="og:image" content="([^"]+)"/);
    if (ogImageMatch && ogImageMatch[1]) {
       // Rewrite 1200x630cw.png to 600x600cc.webp for an avatar square
       let photoUrl = ogImageMatch[1];
       if (photoUrl.includes('1200x630')) {
          photoUrl = photoUrl.replace(/1200x630[a-z]*\.[a-z]+$/, '600x600cc.webp');
       }
       return {
         sourceArtistName: searchData.results[0].artistName,
         sourceArtistImageUrl: photoUrl,
         appleMusicUrl: artistLinkUrl
       };
    }
  } catch (err) {
    console.warn(`Error fetching ${artistName}:`, err.message);
  }
  return null;
}

async function main() {
  const dictionary = await importArtistDictionary();
  let existingData = {};
  
  if (fs.existsSync(TARGET_FILE)) {
    const existingCode = fs.readFileSync(TARGET_FILE, 'utf-8');
    const existingMatch = existingCode.match(/export const LOCAL_IMPORT_ARTISTS_METADATA:[^{]+(\{[\s\S]+\});/);
    if (existingMatch && existingMatch[1]) {
       try {
         existingData = new Function('return ' + existingMatch[1])() || {};
       } catch(e) {}
    }
  }

  const ids = Object.keys(dictionary);
  console.log(`Starting metadata gathering for ${ids.length} artists...`);

  let newMap = { ...existingData };
  let changes = 0;

  for (const id of ids) {
    const artist = dictionary[id];
    const prev = newMap[id];
    
    // Only fetch if missing or pending
    if (!prev || prev.fetchStatus === 'pending') {
       console.log(`[Fetching] ${id} ("${artist.names.en || artist.names.zhHans}")`);
       const queryName = artist.names.zhHans || artist.names.en; // Prio native name for better matches
       
       let fetched = await fetchAppleMusicArtistImage(queryName);
       if (!fetched && artist.names.zhHans && artist.names.en && artist.names.zhHans !== artist.names.en) {
          console.log(`  -> Retrying with en: ${artist.names.en}`);
          await new Promise(r => setTimeout(r, 500));
          fetched = await fetchAppleMusicArtistImage(artist.names.en);
       }

       if (fetched) {
         newMap[id] = {
           id,
           sourceArtistName: fetched.sourceArtistName,
           sourceArtistImageUrl: fetched.sourceArtistImageUrl,
           appleMusicUrl: fetched.appleMusicUrl,
           fetchStatus: 'success',
         };
         console.log(`  -> Success: ${fetched.sourceArtistImageUrl}`);
       } else {
         newMap[id] = {
           id,
           fetchStatus: 'not_found'
         };
         console.log(`  -> Not Found`);
       }
       changes++;
       // sleep 1s
       await new Promise(r => setTimeout(r, 1000));
    }
  }

  const fileContent = `/**
 * This file is automatically generated by scripts/generate-local-artists-metadata.mjs.
 * Do not edit manually. Run the script to regenerate.
 */

export const LOCAL_IMPORT_ARTISTS_METADATA: Record<string, {
  id: string;
  sourceArtistName?: string;
  sourceArtistImageUrl?: string;
  appleMusicUrl?: string;
  fetchStatus: 'success' | 'pending' | 'failed' | 'not_found';
}> = ${JSON.stringify(newMap, null, 2)};
`;

  fs.writeFileSync(TARGET_FILE, fileContent);
  console.log(`Finished generation. Changed ${changes} artists.`);
}

main().catch(console.error);
